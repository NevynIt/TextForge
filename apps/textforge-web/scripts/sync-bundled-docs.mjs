import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, extname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const rootDir = fileURLToPath(new URL('..', import.meta.url));
const repoRoot = resolve(rootDir, '..', '..');
const docsRoot = resolve(repoRoot, 'docs');
const outputPath = resolve(rootDir, 'src', 'generated', 'bundledDocs.js');

const mimeTypeByExtension = {
  '.md': 'text/markdown',
  '.txt': 'text/plain',
  '.itm': 'text/x-itm',
  '.yaml': 'text/yaml',
  '.yml': 'text/yaml',
  '.json': 'application/json',
  '.xml': 'application/xml',
  '.svg': 'image/svg+xml',
};

const languageIdByExtension = {
  '.md': 'markdown',
  '.itm': 'itm',
  '.yaml': 'yaml',
  '.yml': 'yaml',
  '.json': 'json',
  '.xml': 'xml',
  '.svg': 'svg',
};

const docFilePaths = await collectFiles(docsRoot);
const folders = [...new Set(docFilePaths
  .map((path) => relative(docsRoot, dirname(path)).replaceAll('\\', '/'))
  .filter((path) => path && path !== '.')
  .map((path) => `/docs/${path}`))]
  .sort((left, right) => left.localeCompare(right));

const bundledDocs = [];
for (const filePath of docFilePaths.sort((left, right) => left.localeCompare(right))) {
  const relativePath = relative(docsRoot, filePath).replaceAll('\\', '/');
  const extension = extname(filePath).toLowerCase();
  bundledDocs.push({
    path: `/docs/${relativePath}`,
    title: relativePath.split('/').at(-1),
    text: await readFile(filePath, 'utf8'),
    languageId: languageIdByExtension[extension],
    mimeType: mimeTypeByExtension[extension] ?? 'text/plain',
  });
}

const source = `// Generated by scripts/sync-bundled-docs.mjs. Do not edit by hand.
export const bundledDocFolders = ${JSON.stringify(folders, null, 2)};

export const bundledDocs = ${JSON.stringify(bundledDocs, null, 2)};
`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, source);
console.info(`Synced ${bundledDocs.length} bundled docs into ${relative(repoRoot, outputPath).replaceAll('\\', '/')}.`);

async function collectFiles(directoryPath) {
  const entries = await import('node:fs/promises').then(({ readdir }) =>
    readdir(directoryPath, { withFileTypes: true }),
  );
  const files = [];
  for (const entry of entries) {
    const entryPath = resolve(directoryPath, entry.name);
    if (entry.isDirectory()) {
      files.push(...await collectFiles(entryPath));
      continue;
    }

    if (entry.isFile()) {
      files.push(entryPath);
    }
  }
  return files;
}
